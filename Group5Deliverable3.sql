CREATE TABLE Checks(
    CheckID int PRIMARY KEY IDENTITY(1,1)
    ,Number int NOT NULL
    ,BankRouting int NOT NULL
)

CREATE TABLE States(
    StateID int PRIMARY KEY IDENTITY(1,1)
    ,[Name] nvarchar(20) NOT NULL
    ,Abbreviation nvarchar(3) NOT NULL
)

CREATE TABLE Cities(
    CityID int PRIMARY KEY IDENTITY(1,1)
    ,[Name] nvarchar(100) NOT NULL
    ,StateID int NOT NULL CONSTRAINT City_State REFERENCES States(StateID)
)

CREATE TABLE PhoneNumbers(
    PhoneNumberID int PRIMARY KEY IDENTITY(1,1)
    ,Number bigint
)

CREATE TABLE EmailAddresses(
    EmailAddressID int PRIMARY KEY IDENTITY(1,1)
    ,[Name] nvarchar(150)
)

CREATE TABLE ZipCodes(
    ZipCodeID int PRIMARY KEY IDENTITY(1,1)
    ,Number int
)

CREATE TABLE Addresses(
    AddressID int PRIMARY KEY IDENTITY(1,1)
    ,StreetAddress nvarchar(100) NOT NULL
    ,ZipCodeID int NOT NULL CONSTRAINT Address_ZipCode REFERENCES ZipCodes(ZipCodeID)
    ,CityID int NOT NULL CONSTRAINT Address_City REFERENCES Cities(CityID)
)

CREATE TABLE CreditCardTypes(
    CreditCardTypeID int PRIMARY KEY IDENTITY(1,1)
    ,[Name] nvarchar(40) NOT NULL
)

CREATE TABLE CreditCards(
    CreditCardID int PRIMARY KEY IDENTITY(1,1)
    ,Number bigint NOT NULL
    ,DateOfExpiration datetime NOT NULL
    ,VerificationCode smallint NOT NULL
    ,CreditCardTypeID int NOT NULL CONSTRAINT
    CreditCard_CreditCardType REFERENCES CreditCardTypes(CreditCardTypeID)
)

CREATE TABLE ProductCategories(
    ProductCategoryID int PRIMARY KEY IDENTITY(1,1)
    ,[Type] nvarchar(100) NOT NULL
)

CREATE TABLE People(
    PersonID int PRIMARY KEY IDENTITY(1,1)
    ,FirstName nvarchar(60) NOT NULL
    ,MiddleName nvarchar(60) NOT NULL
    ,LastName nvarchar(60) NOT NULL
    ,EmailAddressID int NOT NULL CONSTRAINT Person_EmailAddress REFERENCES EmailAddresses(EmailAddressID)
    ,AddressID int NOT NULL CONSTRAINT Person_Address REFERENCES Addresses(AddressID)
    ,PhoneNumberID int NOT NULL CONSTRAINT Person_PhoneNumber REFERENCES PhoneNumbers(PhoneNumberID)
)

CREATE TABLE Departments(
    DepartmentID int PRIMARY KEY IDENTITY(1,1)
    ,[Name] nvarchar(15) NOT NULL
)

CREATE TABLE Employees(
    EmployeeID int PRIMARY KEY IDENTITY(1,1)
    ,EmployeeNumber int NOT NULL
    ,DepartmentID int NOT NULL CONSTRAINT Employee_Department REFERENCES Departments(DepartmentID)
    ,PersonID int NOT NULL CONSTRAINT Employee_Person REFERENCES People(PersonID)
)

CREATE TABLE Technicians(
    TechnicianID int PRIMARY KEY IDENTITY (1,1)
    ,TechNumber int NOT NULL
    ,WeeklyHoursWork int NOT NULL
    ,HourlyWage decimal(5,2) NOT NULL
    ,EmployeeID int NOT NULL CONSTRAINT Technician_Employee REFERENCES Employees(EmployeeID)
)

CREATE TABLE Salespersons(
    SalespersonID int PRIMARY KEY IDENTITY(1,1)
    ,SalesNumber int NOT NULL
    ,EmployeeID int NOT NULL CONSTRAINT Salesperson_Employee REFERENCES Employees(EmployeeID)
)

CREATE TABLE Customers(
    CustomerID int PRIMARY KEY IDENTITY(1,1)
    ,CustomerNumber int NOT NULL
    ,PersonID int NOT NULL CONSTRAINT Customer_Person REFERENCES People(PersonID)
)

CREATE TABLE ReasonForReturns(
    ReasonForReturnID int PRIMARY KEY IDENTITY(1,1)
    ,Reason nvarchar(80) NOT NULL
)

CREATE TABLE Suppliers(
    SupplierID int PRIMARY KEY IDENTITY(1,1)
    ,Number int
    ,[Name] nvarchar(150) NOT NULL
    ,PhoneNumberID int NOT NULL CONSTRAINT Supplier_PhoneNumber REFERENCES PhoneNumbers(PhoneNumberID)
    ,AddressID int NOT NULL CONSTRAINT Supplier_Address REFERENCES Addresses(AddressID)
    ,EmailAddressID int NOT NULL CONSTRAINT Supplier_EmailAddress REFERENCES EmailAddresses(EmailAddressID)
)

CREATE TABLE ItemDetails(
    ItemDetailID int PRIMARY KEY IDENTITY(1,1)
    ,[Name] nvarchar(100) NOT NULL
    ,[Description] nvarchar(250) NOT NULL
    ,ModelNumber nvarchar(25) NOT NULL
    ,UnitPrice decimal(10,2) NOT NULL
    ,ProductCategoryID int NOT NULL CONSTRAINT Item_ProductCategory REFERENCES ProductCategories(ProductCategoryID)
)

CREATE TABLE Items(
    ItemID int PRIMARY KEY IDENTITY(1,1)
    ,InventoryTag int NOT NULL
    ,SerialNumber nvarchar(40) NOT NULL
    ,ItemDetailID int NOT NULL CONSTRAINT Item_ItemDetails REFERENCES ItemDetails(ItemDetailID)
    ,SupplierID int NOT NULL CONSTRAINT Item_Supplier REFERENCES Suppliers(SupplierID)
)

CREATE TABLE SupplyOrders(
    SupplyOrderID int PRIMARY KEY IDENTITY(1,1)
    ,OrderNumber int NOT NULL
    ,DateRequested datetime NOT NULL
    ,DatePurchased datetime NOT NULL
    ,DateRecieved datetime NOT NULL
    ,ItemID int NOT NULL CONSTRAINT SupplyOrder_Item REFERENCES Items(ItemID)
    ,SupplierID int NOT NULL CONSTRAINT SupplyOrder_Supplier REFERENCES Suppliers(SupplierID)
)

CREATE TABLE Inventories(
    InventoryID int PRIMARY KEY IDENTITY(1,1)
    ,ReorderPoint int NOT NULL
    ,ItemDetailID int NOT NULL CONSTRAINT Inventory_Item REFERENCES ItemDetails(ItemDetailID)
)

CREATE TABLE ServiceForms(
    ServiceFormID int PRIMARY KEY IDENTITY(1,1)
    ,DateIssued datetime NOT NULL
    ,ProblemDescription nvarchar(250)
    ,CustomerID int NOT NULL CONSTRAINT ServiceForm_Customer REFERENCES Customers(CustomerID)
    ,TechnicianID int NOT NULL CONSTRAINT ServiceForm_Technician REFERENCES Technicians(TechnicianID)
    ,ItemDetailID int NOT NULL CONSTRAINT ServiceForm_ItemDetail REFERENCES ItemDetails(ItemDetailID)
)

CREATE TABLE [Services](
    ServiceID int PRIMARY KEY IDENTITY(1,1)
    ,RepairNumber int NOT NULL
    ,LaborHours decimal(10,2) NOT NULL
    ,DateBegan datetime NOT NULL
    ,DateEnded datetime NOT NULL
    ,ServiceFormID int NOT NULL CONSTRAINT Service_ServiceForm REFERENCES ServiceForms(ServiceFormID)
)

CREATE TABLE SalesForms(
	SalesFormID int PRIMARY KEY IDENTITY(1,1)
	,DateIssued datetime NOT NULL
	,Amount decimal(10,2) NOT NULL
	,SalespersonID int NOT NULL CONSTRAINT SalesForm_Salesperson REFERENCES Salespersons(SalespersonID)
	,CustomerID int NOT NULL CONSTRAINT SalesForm_Customer REFERENCES Customers(CustomerID)
	,ItemID int NOT NULL CONSTRAINT SalesForm_Item REFERENCES Items(ItemID)
)

CREATE TABLE [Returns](
    ReturnID int PRIMARY KEY IDENTITY(1,1)
    ,DateReturned datetime NOT NULL
    ,[Signature] image NOT NULL
    ,SalespersonID int NOT NULL CONSTRAINT Return_Salesperson REFERENCES Salespersons(SalespersonID)
    ,ReasonForReturnID int NOT NULL CONSTRAINT Return_ReasonForReturn REFERENCES ReasonForReturns(ReasonForReturnID)
    ,SalesFormID int NOT NULL CONSTRAINT Return_SalesForm REFERENCES SalesForms(SalesFormID)
)

CREATE TABLE ReturnedItems(
    ReturnedItemID int PRIMARY KEY IDENTITY(1,1)
    ,ReturnID int NOT NULL CONSTRAINT ReturnedItem_Return REFERENCES [Returns](ReturnID)
    ,ItemID int NOT NULL CONSTRAINT ReturnedItem_Item REFERENCES Items(ItemID)
)

CREATE TABLE CustomerToCreditCards(
    CustomerToCreditCard int PRIMARY KEY IDENTITY(1,1)
    ,CustomerID int CONSTRAINT CustomerToCreditCard_Customer REFERENCES Customers(CustomerID)
    ,CreditCardID int CONSTRAINT CustomerToCreditCard_CreditCard REFERENCES CreditCards(CreditCardID)
)

CREATE TABLE ItemsSold(
    ItemsSoldID int PRIMARY KEY IDENTITY(1,1)
    ,SalesFormID int NOT NULL CONSTRAINT ItemsSold_SalesForm REFERENCES SalesForms(SalesFormID)
    ,ItemID int NOT NULL CONSTRAINT ItemsSold_Item REFERENCES Items(ItemID)
)

CREATE TABLE Payments(
    PaymentID int PRIMARY KEY IDENTITY(1,1)
    ,Amount decimal NOT NULL
    ,SalesFormID int NOT NULL CONSTRAINT Payment_SalesForm REFERENCES SalesForms(SalesFormID)
    ,CreditCardID int NOT NULL CONSTRAINT Payment_CreditCard REFERENCES CreditCards(CreditCardID)
    ,CheckID int NOT NULL CONSTRAINT Payment_Check REFERENCES Checks(CheckID)
)

CREATE TABLE ItemsToServices(
    ItemsToServicesID int PRIMARY KEY IDENTITY(1,1)
    ,ItemID int NOT NULL CONSTRAINT ItemsToServices_Item REFERENCES Items(ItemID)
    ,ServiceID int NOT NULL CONSTRAINT ItemsToServices_Service REFERENCES [Services](ServiceID)
)

